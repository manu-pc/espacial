    public void buscarLibros() {
        ModeloTablaForo m = (ModeloTablaForo) tablaLibros.getModel();
        if (buscaTitulo.getText().isEmpty() && buscaAutor.getText().isEmpty()) {
            m.setFilas(fa.cargarEntradas());
        }
        else if (buscaTitulo.getText().isEmpty()) {
            m.setFilas(fa.buscarEntradasPorAutor(buscaAutor.getText()));
        }
        else {
            m.setFilas(fa.buscarEntradasPorTitulo(buscaTitulo.getText()));
        }
        m.fireTableDataChanged();
    }

    public void abrirEntrada(int fila) {
        ModeloTablaForo modelo = (ModeloTablaForo) tablaLibros.getModel();
        EntradaForo entrada = modelo.obtenerEntradaForo(fila);
        fa.abrirEntrada(entrada);
    }
        public void leerEntrada(int fila) {
        ModeloTablaForo modelo = (ModeloTablaForo) tablaLibros.getModel();
        EntradaForo entrada = modelo.obtenerEntradaForo(fila);
        fa.leerEntrada(entrada);
    }
    

