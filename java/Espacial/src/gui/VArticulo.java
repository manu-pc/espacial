    private void seleccionarPrimero() {
    
        if (tablaArticulo.getRowCount() == 0) {
            return; // Si está vacía, salir del método
        }

        int filaSeleccionada = 0;

        Articulo a = articulos.get(filaSeleccionada);

        // Rellenar los campos de la interfaz con los datos del artículo
        jTextFieldID.setText(String.valueOf(a.getId()));
        jTextFieldID.setEditable(false);
        jTextFieldAutor.setText(a.getAutor());
        selectCuerpo.setSelectedItem(a.getCuerpoCeleste());
        jTextFieldFecha.setText(a.getFechaPublicacion().toString());
        jTextFieldNumPaginas.setText(String.valueOf(a.getNumPaginas()));
        jTextAreaDescripcion.setText(a.getDescripcion());
    }
    
    private void buscarArticulos() {
    
        articulos = fa.obtenerTodosLosArticulos();
        ModeloTablaArticulo mta = (ModeloTablaArticulo) tablaArticulo.getModel();
        mta.setFilas(articulos);
    }

